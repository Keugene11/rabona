// Get all courses for a department as full codes (e.g. ["CSE 114", "CSE 214"])
export function getCoursesForDept(prefix: string): string[] {
  const dept = SBU_COURSES[prefix.toUpperCase()]
  if (!dept) return []
  return dept.courses.map(n => `${prefix.toUpperCase()} ${n}`)
}

// Search courses by query — matches prefix or department name
export function searchCourses(query: string): string[] {
  const q = query.toUpperCase().trim()
  if (q.length < 2) return []

  // Check if query starts with a known prefix (e.g. "CSE 3" or "CSE")
  const match = q.match(/^([A-Z]{2,4})\s*(\d{0,3})$/)
  if (match) {
    const [, prefix, num] = match
    const dept = SBU_COURSES[prefix]
    if (dept) {
      return dept.courses
        .filter(n => n.startsWith(num))
        .map(n => `${prefix} ${n}`)
    }
  }

  // Otherwise search by department name
  const results: string[] = []
  for (const [code, dept] of Object.entries(SBU_COURSES)) {
    if (code.startsWith(q) || dept.name.toUpperCase().includes(q)) {
      results.push(...dept.courses.slice(0, 5).map(n => `${code} ${n}`))
    }
  }
  return results.slice(0, 15)
}

// Returns matching departments for the prefix search step
export function getMatchingDepts(query: string): { code: string; name: string }[] {
  const q = query.toUpperCase().trim()
  if (q.length < 1) return []

  return Object.entries(SBU_COURSES)
    .filter(([code, dept]) =>
      code.startsWith(q) || dept.name.toUpperCase().includes(q)
    )
    .map(([code, dept]) => ({ code, name: dept.name }))
    .slice(0, 8)
}
